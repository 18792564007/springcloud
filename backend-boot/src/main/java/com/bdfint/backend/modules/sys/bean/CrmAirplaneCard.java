package com.bdfint.backend.modules.sys.bean;

public class CrmAirplaneCard {

    private String cardId;//北斗卡ID

	private String airplaneId;//机尾号

    private String type;//机型
    
    private String madeBy;//航空器制造商
    
    private String age;//机龄
    
    private String company;//单位用户
    
    private String factoryTime;//出厂时间

    private String purpose;//用途
    
    private String bdDevice;//北斗设备生产商
    
    private String appUnit;//航空器所属人
    
    private String regTime;//注册时间

	public String getCardId() {
		return cardId;
	}

	public void setCardId(String cardId) {
		this.cardId = cardId;
	}

	public String getAirplaneId() {
		return airplaneId;
	}

	public void setAirplaneId(String airplaneId) {
		this.airplaneId = airplaneId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMadeBy() {
		return madeBy;
	}

	public void setMadeBy(String madeBy) {
		this.madeBy = madeBy;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getFactoryTime() {
		return factoryTime;
	}

	public void setFactoryTime(String factoryTime) {
		this.factoryTime = factoryTime;
	}

	public String getPurpose() {
		return purpose;
	}

	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}

	public String getBdDevice() {
		return bdDevice;
	}

	public void setBdDevice(String bdDevice) {
		this.bdDevice = bdDevice;
	}

	public String getAppUnit() {
		return appUnit;
	}

	public void setAppUnit(String appUnit) {
		this.appUnit = appUnit;
	}

	public String getRegTime() {
		return regTime;
	}

	public void setRegTime(String regTime) {
		this.regTime = regTime;
	}
     
}